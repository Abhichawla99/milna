// Utility functions for SEO optimization
export const seoUtils = {
  // Generate meta description from content
  generateMetaDescription: (content: string, maxLength: number = 160): string => {
    const cleanContent = content.replace(/<[^>]*>/g, '').trim();
    return cleanContent.length > maxLength 
      ? cleanContent.substring(0, maxLength - 3) + '...'
      : cleanContent;
  },

  // Extract keywords from content
  extractKeywords: (content: string): string[] => {
    const words = content.toLowerCase().match(/\b\w+\b/g) || [];
    const wordCount = words.reduce((acc, word) => {
      acc[word] = (acc[word] || 0) + 1;
      return acc;
    }, {} as Record<string, number>);
    
    return Object.entries(wordCount)
      .filter(([word, count]) => count > 2 && word.length > 3)
      .sort(([,a], [,b]) => b - a)
      .slice(0, 10)
      .map(([word]) => word);
  },

  // Generate structured data for blog posts
  generateStructuredData: (post: any) => ({
    "@context": "https://schema.org",
    "@type": "BlogPosting",
    "headline": post.title,
    "description": post.excerpt,
    "author": {
      "@type": "Person",
      "name": post.author
    },
    "datePublished": post.date,
    "dateModified": post.date,
    "publisher": {
      "@type": "Organization",
      "name": "Milna",
      "logo": {
        "@type": "ImageObject",
        "url": "https://milna.ai/milna-symbol.png"
      }
    },
    "mainEntityOfPage": {
      "@type": "WebPage",
      "@id": `https://milna.ai/blog/${post.slug}`
    }
  })
};

